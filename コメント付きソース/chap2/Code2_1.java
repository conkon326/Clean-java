public class Code2_1{
  public static void main(String[] args){

    int a;         //int型の変数aを宣言する
    int b;         //int型の変数bを宣言する

    a = 20;        //変数aに20を代入する
    b = a + 5;     //変数bに､変数aに5を足した結果を代入する

    System.out.println(a);  //変数aを出力改行
    System.out.println(b);  //変数bを出力改行


    b = b - 4;     //変数bに､変数bから4引いた結果を代入する
    System.out.println(b);  //結果を出力

    b = b % 4;     //変数bに､変数bを4で割った余りを代入する
    System.out.println(b);  //結果を出力


  }
}
